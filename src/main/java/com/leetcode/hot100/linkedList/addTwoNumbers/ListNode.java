package com.leetcode.hot100.linkedList.addTwoNumbers;

public class ListNode {
    public int val;
   public ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}